public class SolidRectangle {
    public static void main(String[] args) {
        int lenght = 4;
        int height = 5;
        for(int i=1;i<=lenght;i++){
            for(int j=1;j<=height;j++){
                System.out.print("*");
            }
            System.out.println();
        }
    }
}
